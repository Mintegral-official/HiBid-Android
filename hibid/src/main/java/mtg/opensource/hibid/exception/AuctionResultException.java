package mtg.opensource.hibid.exception;

public class AuctionResultException extends Exception {
    public AuctionResultException() {
    }

    public AuctionResultException(String arg0) {
        super(arg0);
    }

    public AuctionResultException(Throwable arg0) {
        super(arg0);
    }

    public AuctionResultException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }
}
