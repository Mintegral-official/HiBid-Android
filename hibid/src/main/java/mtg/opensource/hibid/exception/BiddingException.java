package mtg.opensource.hibid.exception;

public class BiddingException extends Exception {

	public BiddingException() {
	}

	public BiddingException(String arg0) {
		super(arg0);
	}

	public BiddingException(Throwable arg0) {
		super(arg0);
	}

	public BiddingException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
